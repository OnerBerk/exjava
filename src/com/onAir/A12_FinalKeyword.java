package com.onAir;

public class A12_FinalKeyword {
    public static void main(String[] args) {
        //utiliser le terme final si l'ont veux que la valeur de la variable ne soit pas modifiable
        //par convention on declare en majuscule une variable final

        final int VALUE =9;
        final String PASSWORD = "abcde";

        System.out.println(VALUE);
        System.out.println(PASSWORD);
    }
}
